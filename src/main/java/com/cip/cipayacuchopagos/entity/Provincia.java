package com.cip.cipayacuchopagos.entity;

import lombok.*;

import javax.persistence.*;
import java.io.Serializable;

/**
 *
 * @author Arango
 */
@Entity
@Getter
@Setter
@AllArgsConstructor
@Builder
@Data
@ToString
@RequiredArgsConstructor
@Table(name = "Provincia", schema = "dbo")

public class Provincia implements Serializable {
    private static final long serialVersionUID = 1L;
    @Id
    @Basic(optional = false)
    @GeneratedValue(strategy = GenerationType.SEQUENCE, generator = "generator_Provincia")
    @SequenceGenerator(name = "generator_Provincia", sequenceName = "seq_Provincia", allocationSize = 1)
    @Column(name = "idprovincia")
    private Integer idprovincia;
    @Basic(optional = false)
    @Column(name = "descripcion")
    private String descripcion;
    @Basic(optional = false)
    @Column(name = "estado")
    private boolean estado;

    @Column(name = "iddepartamento")
    private Integer iddepartamento;

}
